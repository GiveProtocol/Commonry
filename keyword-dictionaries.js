/**
 * Get all keywords as a flat array with their domains
 * Useful for building search indexes
 */
export function getAllKeywordsWithDomains() {
  const result = [];
  for (const [domain, keywords] of Object.entries(domainKeywords)) {
    for (const keyword of keywords) {
      result.push({ keyword, domain });
    }
  }
  return result;
}

